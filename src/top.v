// =============================================================================
// top.v  --  里程碑 1：撥開關控 LED
// 目的：把整條工具鏈跑通（RTL -> XDC -> build.tcl -> bitstream -> 燒錄）。
//
// 設計刻意簡單到不可能出錯：8 顆開關直接接 8 顆 LED，純組合邏輯，無時脈。
// 這樣任何問題都能歸因到「工具鏈/約束」而非「邏輯」。
// =============================================================================

module top (
    input  wire [7:0] sw,    // 8 個滑動開關 SW0..SW7
    output wire [7:0] led    // 8 顆 LED       LD0..LD7
);

    // 開關狀態直接驅動對應的 LED。
    assign led = sw;

endmodule
